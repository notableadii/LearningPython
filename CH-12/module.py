def myFunc():
    print("Hello World")


if __name__ == "__main__":
    myFunc()
# This code imports the `myFunc` function from the `module` module and calls it.
# It demonstrates how to use the `import` statement to bring in functionality from another module.
# The `if __name__ == "__main__":` block ensures that the code inside it runs only when the script is executed directly,
# not when it is imported as a module in another script.
# This is useful for testing or running specific code when the script is executed as the main program.
# The `myFunc` function is expected to be defined in the `module` module.
# The code structure allows for modular programming, making it easier to maintain and organize code.
