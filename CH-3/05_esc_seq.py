# escape sequence characters

# \n - newline

a = "Hello\nWorld" #this will print Hello and World in two lines

print(a) # Hello
# World

# \t - tab

a = "Hello\tWorld" #this will give a tab space between Hello and World
print(a) # Hello	World

# \b - backspace

a = "Hello\bWorld" #this will remove the last character of Hello

print(a) # HellWorld

# \" - double quote"

a = "Hello \"World\"" #this will print Hello and World in double quotes

print(a) # Hello"World

